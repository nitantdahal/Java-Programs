public class Printype {
    public static void main(String[] args) {
        System.out.println("Hello World"); // This prints and goes to new/next line 
        System.out.print("Hello World"); // This prints and does not go to next line  
        System.out.print("Hello World");
        System.out.println("Hello World");

        // Comments (Multiline Comment)
        /*
        Hello
        World
        */

        // Java Doctype Comment 
       /**
        * Hello This is my
        * first program
        */
    }
}